# ── Clean + validate Indian plate ───────────────────────────────
def parse_plate(raw):
    text = re.sub(r'[^A-Z0-9\s\-]', '', raw.upper()).strip()
    m = INDIA_RE.search(text)
    if m:
        return "".join(m.groups()), True
    return text or raw, False


# ── Enhance small crop ──────────────────────────────────────────
def enhance(crop_bgr):
    h, w = crop_bgr.shape[:2]
    if h < 64:
        crop_bgr = cv2.resize(crop_bgr, (int(w * 64/h), 64), interpolation=cv2.INTER_CUBIC)
    k = np.array([[0,-1,0],[-1,5,-1],[0,-1,0]])
    return cv2.filter2D(crop_bgr, -1, k)


# ── Full pipeline for one image ──────────────────────────────────
def read_plate(image_path):
    """
    Returns:
        img_rgb  : original image as RGB numpy array
        boxes    : list of [x1,y1,x2,y2]
        plates   : list of plate strings (same length as boxes)
        valids   : list of bools
    """
    img_bgr = cv2.imread(str(image_path))
    if img_bgr is None:
        raise FileNotFoundError(image_path)
    img_rgb = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2RGB)
    H, W    = img_bgr.shape[:2]

    boxes = detect_plates(img_bgr)

    if not boxes:
        # no box found — run OCR on full image
        raw           = glm_ocr(Image.fromarray(img_rgb))
        plate, valid  = parse_plate(raw)
        return img_rgb, [[0, 0, W, H]], [plate], [valid]

    plates, valids = [], []
    for (x1, y1, x2, y2) in boxes:
        pad = 8
        crop = img_bgr[max(0,y1-pad):min(H,y2+pad),
                       max(0,x1-pad):min(W,x2+pad)]
        crop = enhance(crop)
        raw  = glm_ocr(Image.fromarray(cv2.cvtColor(crop, cv2.COLOR_BGR2RGB)))
        plate, valid = parse_plate(raw)
        plates.append(plate)
        valids.append(valid)

    return img_rgb, boxes, plates, valids



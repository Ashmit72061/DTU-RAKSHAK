def extract_plate_structured(image_path: str) -> dict:
    """
    Uses GLM-OCR's information extraction mode to return structured JSON.
    Best for high-quality cropped plate images.
    """
    pil_image = Image.open(image_path).convert("RGB")
    raw = run_glm_ocr(pil_image, prompt=PLATE_EXTRACTION_PROMPT)

    # Parse JSON from response
    try:
        # Strip any markdown fences
        clean = re.sub(r'```json|```', '', raw).strip()
        data = json.loads(clean)
    except json.JSONDecodeError:
        # Fallback: try to extract plate number at minimum
        data = {"plate_number": parse_indian_plate(raw), "raw": raw}

    print(json.dumps(data, indent=2))
    return data



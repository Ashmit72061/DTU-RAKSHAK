def annotate_image(image_bgr: np.ndarray, results: list[dict]) -> np.ndarray:
    """
    Draw boxes and plate text on the image.
    """
    annotated = image_bgr.copy()
    for r in results:
        x1, y1, x2, y2 = r["bbox"]
        plate = r["plate_text"]
        conf  = r.get("confidence", 0)

        color = (0, 200, 50) if r.get("valid_format") else (0, 165, 255)
        cv2.rectangle(annotated, (x1, y1), (x2, y2), color, 3)

        label = f"{plate}  ({conf:.0%})"
        (tw, th), _ = cv2.getTextSize(label, cv2.FONT_HERSHEY_SIMPLEX, 0.9, 2)
        cv2.rectangle(annotated, (x1, y1 - th - 12), (x1 + tw + 8, y1), color, -1)
        cv2.putText(annotated, label, (x1 + 4, y1 - 4),
                    cv2.FONT_HERSHEY_SIMPLEX, 0.9, (255, 255, 255), 2)

    return annotated

def process_image(image_path: str, save_result: bool = True, show: bool = True) -> list[dict]:
    """
    Full pipeline: detect plates → crop → OCR → parse → annotate.

    Args:
        image_path  : path to input image
        save_result : save annotated image alongside the input
        show        : display result inline (Colab)

    Returns:
        List of result dicts with keys:
        bbox, confidence, raw_ocr, plate_text, valid_format
    """
    print(f"\n🔍  Processing: {image_path}")

    # ---- Load image ----
    image_bgr = cv2.imread(image_path)
    if image_bgr is None:
        raise FileNotFoundError(f"Could not read: {image_path}")
    h, w = image_bgr.shape[:2]
    print(f"    Image size: {w}×{h}")

    # ---- Detect plates ----
    detections = detect_plates(image_bgr)
    print(f"    Plates found: {len(detections)}")

    if not detections:
        print("    ⚠️  No plates detected. Running GLM-OCR on full image as fallback.")
        pil_full = Image.fromarray(cv2.cvtColor(image_bgr, cv2.COLOR_BGR2RGB))
        raw = run_glm_ocr(pil_full)
        plate = parse_indian_plate(raw)
        valid = bool(INDIA_PLATE_REGEX.search(plate))
        print(f"    📋 Full-image OCR: {plate} {'✅' if valid else '❓'}")
        return [{"bbox": [0, 0, w, h], "confidence": 0.0,
                 "raw_ocr": raw, "plate_text": plate, "valid_format": valid}]

    results = []
    for i, det in enumerate(detections):
        x1, y1, x2, y2 = det["bbox"]

        # Expand crop slightly for better OCR
        x1c = max(0, x1 - EXPAND_PADDING)
        y1c = max(0, y1 - EXPAND_PADDING)
        x2c = min(w, x2 + EXPAND_PADDING)
        y2c = min(h, y2 + EXPAND_PADDING)

        crop_bgr = image_bgr[y1c:y2c, x1c:x2c]

        # Enhance contrast for better OCR
        crop_bgr = enhance_plate_crop(crop_bgr)

        pil_crop = Image.fromarray(cv2.cvtColor(crop_bgr, cv2.COLOR_BGR2RGB))

        raw = run_glm_ocr(pil_crop)
        plate = parse_indian_plate(raw)
        valid = bool(INDIA_PLATE_REGEX.search(plate))

        result = {
            "bbox": [x1, y1, x2, y2],
            "confidence": det["confidence"],
            "raw_ocr": raw,
            "plate_text": plate,
            "valid_format": valid,
        }
        results.append(result)
        status = "✅" if valid else "❓"
        print(f"    [{i+1}] {plate}  conf={det['confidence']:.0%}  {status}  (raw: {repr(raw)})")

    # ---- Annotate & display ----
    annotated = annotate_image(image_bgr, results)

    out_path = str(Path(image_path).with_suffix("")) + "_result.jpg"
    if save_result:
        cv2.imwrite(out_path, annotated)
        print(f"    💾 Saved: {out_path}")

    if show:
        try:
            from IPython.display import display
            display(Image.fromarray(cv2.cvtColor(annotated, cv2.COLOR_BGR2RGB)))
        except ImportError:
            pass

    return results


def enhance_plate_crop(crop_bgr: np.ndarray) -> np.ndarray:
    """
    Simple preprocessing to improve OCR accuracy on plate crops.
    """
    # Resize to a standard height while preserving aspect ratio
    target_h = 80
    h, w = crop_bgr.shape[:2]
    if h < target_h:
        scale = target_h / h
        crop_bgr = cv2.resize(crop_bgr, (int(w * scale), target_h), interpolation=cv2.INTER_CUBIC)

    # Sharpen
    kernel = np.array([[0, -1, 0], [-1, 5, -1], [0, -1, 0]])
    crop_bgr = cv2.filter2D(crop_bgr, -1, kernel)

    return crop_bgr

def process_folder(folder_path: str, extensions: tuple = (".jpg", ".jpeg", ".png")) -> list[dict]:
    """
    Process all images in a folder and return a combined results list.
    """
    all_results = []
    images = [
        str(p) for p in Path(folder_path).iterdir()
        if p.suffix.lower() in extensions
    ]
    print(f"Found {len(images)} image(s) in '{folder_path}'")

    for img_path in images:
        try:
            results = process_image(img_path, save_result=True, show=True)
            for r in results:
                r["source_file"] = img_path
            all_results.extend(results)
        except Exception as e:
            print(f"  ❌ Error on {img_path}: {e}")

    # Summary
    print("\n" + "="*50)
    print("📊  SUMMARY")
    print("="*50)
    valid_count = sum(1 for r in all_results if r["valid_format"])
    print(f"Total plates detected : {len(all_results)}")
    print(f"Valid Indian format   : {valid_count}")
    print(f"Unconfirmed           : {len(all_results) - valid_count}")
    print("-"*50)
    for r in all_results:
        src = Path(r.get("source_file", "")).name
        status = "✅" if r["valid_format"] else "❓"
        print(f"  {status}  {r['plate_text']:<15}  [{src}]")

    return all_results


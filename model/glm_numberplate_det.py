def detect_plates_yolo(image_bgr: np.ndarray) -> list[dict]:
    """
    Returns a list of dicts: {bbox: [x1,y1,x2,y2], confidence: float}
    """
    results = plate_detector(image_bgr, verbose=False)[0]
    plates = []
    for box in results.boxes:
        conf = float(box.conf[0])
        if conf < CONFIDENCE_THRESHOLD:
            continue
        x1, y1, x2, y2 = map(int, box.xyxy[0])
        plates.append({"bbox": [x1, y1, x2, y2], "confidence": conf})
    return plates


def detect_plates_contour(image_bgr: np.ndarray) -> list[dict]:
    """
    Fallback: simple OpenCV contour + aspect-ratio heuristic for plates.
    Works best when plate is clear and front-facing.
    """
    gray   = cv2.cvtColor(image_bgr, cv2.COLOR_BGR2GRAY)
    blur   = cv2.bilateralFilter(gray, 11, 17, 17)
    edged  = cv2.Canny(blur, 30, 200)
    cnts, _ = cv2.findContours(edged.copy(), cv2.RETR_TREE, cv2.CHAIN_APPROX_SIMPLE)
    cnts   = sorted(cnts, key=cv2.contourArea, reverse=True)[:20]

    plates = []
    for c in cnts:
        peri = cv2.arcLength(c, True)
        approx = cv2.approxPolyDP(c, 0.018 * peri, True)
        if len(approx) == 4:
            x, y, w, h = cv2.boundingRect(approx)
            aspect = w / float(h)
            # Indian plates ≈ 2.0–5.5 aspect ratio
            if 2.0 <= aspect <= 5.5 and w > 80:
                plates.append({"bbox": [x, y, x+w, y+h], "confidence": 0.5})
    return plates


def detect_plates(image_bgr: np.ndarray) -> list[dict]:
    if plate_detector:
        return detect_plates_yolo(image_bgr)
    return detect_plates_contour(image_bgr)

def run_glm_ocr(pil_image: Image.Image, prompt: str = "Text Recognition:") -> str:
    """
    Run GLM-OCR on a PIL image with the given prompt.
    """
    # Save to a temp file (GLM-OCR's processor expects a file path or URL)
    tmp_path = "/tmp/_glm_plate.png"
    pil_image.save(tmp_path)

    messages = [
        {
            "role": "user",
            "content": [
                {"type": "image", "url": tmp_path},
                {"type": "text",  "text": prompt}
            ],
        }
    ]

    inputs = processor.apply_chat_template(
        messages,
        tokenize=True,
        add_generation_prompt=True,
        return_dict=True,
        return_tensors="pt",
    ).to(ocr_model.device)
    inputs.pop("token_type_ids", None)

    with torch.no_grad():
        generated_ids = ocr_model.generate(**inputs, max_new_tokens=128)

    output = processor.decode(
        generated_ids[0][inputs["input_ids"].shape[1]:],
        skip_special_tokens=True
    )
    return output.strip()



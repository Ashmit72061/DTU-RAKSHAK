def parse_indian_plate(raw_text: str) -> str:
    """
    Clean up OCR noise and try to match standard Indian plate format.
    Returns cleaned plate string or the raw text if no match found.
    """
    # Normalize: uppercase, remove common OCR noise
    text = raw_text.upper()
    text = re.sub(r'[^A-Z0-9\s\-]', '', text)   # keep alphanumeric + space + dash
    text = re.sub(r'\s+', ' ', text).strip()

    match = INDIA_PLATE_REGEX.search(text)
    if match:
        state, dist, series, number = match.groups()
        return f"{state}{dist}{series}{number}"

    # Fallback: return cleaned text
    return text


